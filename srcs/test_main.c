#include <stdio.h>
#include <stdlib.h>
#include "../includes/libft.h"

void    test_ftstrcpy(char *str);

int     main(void)
{
    /* ************************************************************************** */
    /* ft_strlen.c                                                                */
    /* -------------------------------------------------------------------------- */
    /* Depends on:                                                                */
    /* -------------------------------------------------------------------------- */
    /* Function: Return length of string                                          */
    /* -------------------------------------------------------------------------- */
    /* Prototype: int   ft_strlen(char *str)                                      */
    /* ************************************************************************** */

    printf("*********************\n");
    printf("ft_strlen (of string \"Hello!\"): %d\n", ft_strlen("Hello!"));
    printf("*********************\n");

    printf("\n");

    /* ************************************************************************** */
    /* ft_strdup.c                                                                */
    /* -------------------------------------------------------------------------- */
    /* Depends on: stdlib.h(malloc)                                               */
    /* -------------------------------------------------------------------------- */
    /* Function: Return a duplicate of the given string                           */
    /* -------------------------------------------------------------------------- */
    /* Prototype: char  *ft_strdup(char *str)                                     */
    /* ************************************************************************** */

    printf("*********************\n");
    printf("ft_strdup (of string \"Hello!\"): %s\n", ft_strdup("Hello!"));
    printf("*********************\n");
    
    printf("\n");

    /* ************************************************************************** */
    /* ft_strcpy.c                                                                */
    /* -------------------------------------------------------------------------- */
    /* Depends on:                                                                */
    /* -------------------------------------------------------------------------- */
    /* Function: Copy the string pointed to by src (including '\0') to dest       */
    /* -------------------------------------------------------------------------- */
    /* Prototype: void  ft_strcpy(char *dest, char *src)                          */
    /* ************************************************************************** */

    printf("*********************\n");
    printf("ft_strcpy (of string \"Hello!\"): ");
    test_ftstrcpy("Hello!");
    printf("\n*********************\n");
    return (0);
}

void    test_ftstrcpy(char *str)
{
    int     len;
    char    *dest;

    len = ft_strlen(str);
    dest = malloc(sizeof(char) * len + 1);
    ft_strcpy(dest, str);
    printf("%s", dest);
}

